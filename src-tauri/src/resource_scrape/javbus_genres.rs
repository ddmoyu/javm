// ─── 平铺查找表（从原始数据构建） ───

fn build_lookup(entries: &[GenreEntry]) -> HashMap<&'static str, &'static str> {
    let mut map = HashMap::with_capacity(entries.len());
    for e in entries {
        map.insert(e.name, e.id);
    }
    map
}

static CENSORED_LOOKUP: LazyLock<HashMap<&'static str, &'static str>> =
    LazyLock::new(|| build_lookup(CENSORED_ENTRIES));

static UNCENSORED_LOOKUP: LazyLock<HashMap<&'static str, &'static str>> =
    LazyLock::new(|| build_lookup(UNCENSORED_ENTRIES));

// ─── 分组输出（供前端展示） ───

/// 将有码+无码分类分别按大类别分组返回。
pub fn all_genre_groups() -> JavbusGenreData {
    fn build_groups(entries: &[GenreEntry]) -> Vec<GenreGroup> {
        let mut seen_cats: Vec<&str> = Vec::new();
        let mut cat_map: HashMap<&str, Vec<&str>> = HashMap::new();
        for e in entries {
            let names = cat_map.entry(e.category).or_default();
            if !names.contains(&e.name) {
                if !seen_cats.contains(&e.category) {
                    seen_cats.push(e.category);
                }
                names.push(e.name);
            }
        }
        let mut groups = Vec::new();
        for cat in &seen_cats {
            if let Some(names) = cat_map.remove(cat) {
                let mut sorted: Vec<String> = names.into_iter().map(|s| s.to_string()).collect();
                sorted.sort_unstable_by(|a, b| a.cmp(b));
                groups.push(GenreGroup {
                    category: cat.to_string(),
                    genres: sorted,
                });
            }
        }
        groups
    }

    JavbusGenreData {
        censored: build_groups(CENSORED_ENTRIES),
        uncensored: build_groups(UNCENSORED_ENTRIES),
    }
}

/// 返回全部硬编码 JavBus 分类名（有码+无码合并去重），供前端发现页展示（兼容旧接口）。
pub fn all_genre_names() -> Vec<&'static str> {
    let mut names: Vec<&str> = CENSORED_LOOKUP.keys().copied().collect();
    for k in UNCENSORED_LOOKUP.keys() {
        if !CENSORED_LOOKUP.contains_key(k) {
            names.push(k);
        }
    }
    names.sort_unstable_by(|a, b| a.cmp(b));
    names
}

