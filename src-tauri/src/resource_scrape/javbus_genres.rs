/// 在硬编码查找表中定位分类的 source_id。
pub fn lookup_genre_source_id(name: &str, lane: Lane) -> Option<&'static str> {
    let name = name.trim();
    if name.is_empty() {
        return None;
    }

    let table: &HashMap<&str, &str> = match lane {
        Lane::Censored => &CENSORED_LOOKUP,
        Lane::Uncensored => &UNCENSORED_LOOKUP,
    };

    if let Some(id) = table.get(name) {
        return Some(id);
    }

    let traditional = to_traditional(name);
    if traditional != name {
        if let Some(id) = table.get(traditional.as_str()) {
            return Some(id);
        }
    }

    None
}

